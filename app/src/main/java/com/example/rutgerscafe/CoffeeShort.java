package com.example.rutgerscafe;

/**
 * This class extends the CoffeeType class and includes specific data and operations in regards to coffee size short.
 *
 * @author Divya Bhuva, Dorothy Wu
 */
public class CoffeeShort extends CoffeeType{

	/**
	 * This is the no argument CoffeeShort constructor.
	 * It sets the size to "Short" and the price increase.
	 */
	CoffeeShort(){
		priceIncrease = 0.00;
		sizeofCoffee = "Short";
	}
}

